#Haz un programa que pida dos numero y muestre si el primero es mayor, menor o igual al segundo

num1 = int(input("Ingresa el primer numero: "))
num2 = int(input("Ingresa el segundo numero: "))

print(f"{num1} es mayor que {num2}? {num1 > num2}")
print(f"{num1} es menor que {num2}? {num1 < num2}")
print(f"{num1} es igual que {num2}? {num1 == num2}")
